package com.penjat.jarrega_ezquerro_practica_android_m08uf2;

public class Word {
    private String id;
    private String word;

    public Word() {
    }

    public Word(String id, String word) {
        this.id = id;
        this.word = word;
    }

    public String getId() {
        return id;
    }

    public String getWord() {
        return word;
    }
}
