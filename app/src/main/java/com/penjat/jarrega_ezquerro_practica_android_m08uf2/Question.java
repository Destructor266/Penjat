package com.penjat.jarrega_ezquerro_practica_android_m08uf2;

public class Question {
    private String id;
    private String question;

    public Question() {
    }

    public Question(String id, String question) {
        this.id = id;
        this.question = question;
    }

    public String getId() {
        return id;
    }

    public String getQuestion() {
        return question;
    }
}
